// Centralized project data used by Home, Work, and ProjectDetail pages
//
// VIDEO REELS: Each reel has a `video` field pointing to /videos/{slug}/reel-{n}.mp4
// To add your videos: drop MP4 files into public/videos/{slug}/ matching these names.
// See public/videos/README.md for the full guide.

function buildReels(slug, labels) {
    return labels.map((label, i) => ({
        id: i + 1,
        label,
        video: `/videos/${slug}/reel-${i + 1}.mp4`,
        gradient: `linear-gradient(180deg, hsl(${(i * 47 + 200) % 360}, 30%, 8%) 0%, hsl(${(i * 47 + 220) % 360}, 35%, 18%) 50%, hsl(${(i * 47 + 200) % 360}, 30%, 6%) 100%)`,
    }));
}

